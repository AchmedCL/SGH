    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        String nombre = this.nombre.getText();
        if(sec.validarRut(this.run.getText())==true)
        {
            String rut = this.run.getText();
            String nombreMadre = this.nombremadre.getText();
            String nombrePadre = this.nombrepadre.getText();
            if(sec.validarNumeros(this.edad.getText())==true)
            {
                int edad = Integer.parseInt(this.edad.getText());
                String sexo = this.sexo.getText();
                //Date fecha = fechaActual;
                if(hospi.agregarPaciente(nombre,rut,nombreMadre,nombrePadre,edad,sexo,fechaActual,null)==true)
                {
                    VentanaSucess ok = new VentanaSucess(evt,hospi);
                    ok.setVisible(true);
                    ok.setLocationRelativeTo(null);
                    VentanaAgregarPaciente.this.dispose();
                }
                else
                {
                    VentanaFail ok = new VentanaFail(evt,hospi);
                    ok.setVisible(true);
                    ok.setLocationRelativeTo(null);
                    VentanaAgregarPaciente.this.dispose();
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "La edad ingresada debe contener solo numeros");
                int valor=JOptionPane.showConfirmDialog(this,"¿Desea ingresar nuevamente la edad?","Pregunta",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
                if(valor==JOptionPane.NO_OPTION)
                {
                    VentanaFail ok = new VentanaFail(evt,hospi);
                    ok.setVisible(true);
                    ok.setLocationRelativeTo(null);
                    VentanaAgregarPaciente.this.dispose();
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "El rut ingresado no es valido");
            int valor=JOptionPane.showConfirmDialog(this,"¿Desea ingresar nuevamente el rut?","Pregunta",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(valor==JOptionPane.NO_OPTION)
            {
                VentanaFail ok = new VentanaFail(evt,hospi);
                ok.setVisible(true);
                ok.setLocationRelativeTo(null);
                VentanaAgregarPaciente.this.dispose();
            }
        }
    }//GEN-LAST:event_botonAgregarActionPerformed


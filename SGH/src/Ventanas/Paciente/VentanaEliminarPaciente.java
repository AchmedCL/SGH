    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        String rut = this.rutPaciente.getText();
        if(hospi.buscarPaciente(rut)!=null)
        {
            if(hospi.buscarPaciente(rut).getCodigoCama()!=null)
            {
                if(hospi.buscarCamaEspecifica(hospi.buscarPaciente(rut).getCodigoCama()).vaciarCama()==true)
                {
                    if(hospi.eliminarPaciente(rut)==true){
                        VentanaSucess ok = new VentanaSucess(evt,hospi);
                        ok.setVisible(true);
                        ok.setLocationRelativeTo(null);
                        VentanaEliminarPaciente.this.dispose();
                    } else {
                        VentanaFail ok = new VentanaFail(evt,hospi);
                        ok.setVisible(true);
                        ok.setLocationRelativeTo(null);
                        VentanaEliminarPaciente.this.dispose();
                    }
                }
            }
        }
    }//GEN-LAST:event_EliminarActionPerformed


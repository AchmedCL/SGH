    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        String rut = this.run.getText();
        final VentanaModificarEnfermero vr ;
        if(hospi.buscarEnfermeria(rut)!=null)
        {
            vr = new VentanaModificarEnfermero(evt,hospi,hospi.buscarEnfermeria(rut).getRut());
            botonBuscar.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    try {
                        vr.setVisible(true);
                        vr.setLocationRelativeTo(null);
                        VentanaBuscarEnfermeroModificar.this.dispose();
                    } catch (final Exception excep) {
                        System.exit(0);
                    } 
                }
            });
        }else
        {
            VentanaFail ok = new VentanaFail(evt,hospi);
            ok.setVisible(true);
            ok.setLocationRelativeTo(null);
            VentanaBuscarEnfermeroModificar.this.dispose();
        }
    }//GEN-LAST:event_botonBuscarActionPerformed

